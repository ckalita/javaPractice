package String;

public class StringSub {

	public static void main(String[] args) {
		String s = "Chandan";
		
		System.out.println(s.substring(2)); //andan
		System.out.println(s.substring(2,5)); //and
		System.out.println(s.substring(0,4)); //Chan
		
		int i =1;
		System.out.println(i++);//1
		System.out.println(++i);//3
	}

}
